## Crie um programa onde peça o nome:


## Variável nome criada.
nome = input("Olá, qual é o seu nome? ")

## Primeira forma:
print("Seja bem vindo ", nome, "!")
print("Seja bem vindo " + nome + " !")

## Segunda forma:
print("Seja bem vindo {} !".format(nome))

## Terceira forma:
print(f"Seja bem vindo {nome} !")



