## MAX e MIN : Funções que mostram o máximo ou p mínimo de uma lista, max(lista), min(lista):
notas = [4.5,5.9,10,2,6.7,1.04,9.9]
temperaturas =[27.4,29,28.0,35.5,-1.3,17.8]
print(min(notas))
print(max(notas))
