## PRINCIPAIS FUNÇÕES USADAS NO PYTHON:

## POW : Função de potência pow(base,potência)
print(pow(2,3))
print(pow(5,2))
print(pow(2,10))

print("_____"*10)## espaço
## ROUND : Função de arredondamento round(valor, quantidade de casas decimais)
print(round(3.141578699000,2))
print(round(23.7657657,1))
print(round(34.6786985739778,3))

print("_____"*10)## espaço

## SUM : Função que soma uma lista e acrescenta valor no resultado, sum(lista, X):
minhaLista = [1,2,3,4]
listinha = [5,5,10,10]
print(sum(minhaLista))
print(sum(listinha,1))

