# EXPLIACAÇÃO
# Função é um comando que pode ser criado quando há a necessidade do
# mesmo ser repetido várias vezes.
#
# DEF : È o comando que DEFINE uma FUNÇÃO:
# MENSG : É o nome que foi atibuído á FUNÇÃO.
# MSG : É o nome do parâmetro que vai ser alterado conforme a necessidade.
#
# EXEMPLO:


def mensg(msg):
    print("-----------------------------------")
    print(msg)
    print("-----------------------------------")


mensg("Eu sou aluno");
mensg("Meu nome é Rômulo");
